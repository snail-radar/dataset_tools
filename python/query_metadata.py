def query_radar_metadata(condition):
    results = []

    if isinstance(condition, str) and condition in radar_metadata:
        return radar_metadata[condition]

    for data_id, data in radar_metadata.items():
        match = True
        for key, value in condition.items():
            if data.get(key) != value:
                match = False
                break
        if match:
            results.append(data_id)
    return results


